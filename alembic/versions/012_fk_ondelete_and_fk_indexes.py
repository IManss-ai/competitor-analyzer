"""add ON DELETE behavior to every FK + index hot FK columns

Finding B (audit 2026-07-21): every ForeignKey in app/models.py was a bare FK
with no ondelete — hard-deleting a user or competitor was impossible (FK
violations from up to 9 child tables) and account deletion needed hand-written
cascade scripts. This migration recreates every FK constraint with the ON
DELETE behavior now declared in app/models.py:

    CASCADE  — all child rows (snapshots, change_events, reviews,
               review_snapshots, social_posts, job_postings, job_snapshots,
               battlecard_cache, campaigns -> competitors; competitors,
               campaigns, approved_actions, magic_link_tokens -> users;
               approved_actions -> change_events; action_plans -> campaigns;
               action_plan_items -> action_plans; geo_snapshots -> campaigns;
               app_pricing/app_tech -> apps; change_events -> snapshots, which
               are NOT NULL so SET NULL is impossible there)
    SET NULL — competitors.app_id -> apps (nullable discovery link)

The FK recreation is POSTGRES-ONLY (dialect-guarded), following the style of
revision 851bf9d63155: SQLite cannot ALTER constraints without a full
batch-mode table rebuild of 15 tables, dev SQLite DBs are disposable, and
SQLite does not enforce FKs in this app anyway (no PRAGMA foreign_keys=ON).
Fresh dev DBs get the ondelete behavior from app/models.py via create_all.

Constraint names are NOT hardcoded: prod constraints carry Postgres
autogenerated names (e.g. snapshots_competitor_id_fkey, or
fk_competitors_app_id which was explicitly named in 851bf9d63155), so each FK
is looked up by (constrained column, referred table) via the inspector and
recreated UNDER ITS EXISTING NAME. FKs already carrying the right ondelete are
skipped — the migration is idempotent / safe to re-run.

Finding C: Postgres does not auto-index FK columns; the dashboard's hottest
queries (app/routes/api_v1.py ~184-264) GROUP BY competitor_id with
MAX(fetched_at) / MAX(snapshot_at) per competitor. Composite/plain indexes are
created here (both dialects, IF NOT EXISTS) matching the declarations added to
app/models.py.

Revision ID: 012
Revises: 011
Create Date: 2026-07-23

"""
