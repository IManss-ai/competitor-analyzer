def _set_fk_ondelete(target_ondelete_by_rule) -> None:
    """Drop + recreate each FK (Postgres only) with the desired ON DELETE,
    keeping its existing (autogenerated or explicit) constraint name."""
    bind = op.get_bind()
    if bind.dialect.name != "postgresql":
        return  # see module docstring: SQLite dev DBs rely on models.py/create_all
    inspector = sa.inspect(bind)
    # Snapshot all FK metadata up front so drops don't confuse later lookups.
    fks_by_table = {
        table: inspector.get_foreign_keys(table)
        for table in {rule[0] for rule in FK_RULES}
        if inspector.has_table(table)
    }
    for table, column, ref_table, _ in FK_RULES:
        desired = target_ondelete_by_rule((table, column, ref_table))
        for fk in fks_by_table.get(table, []):
            if fk["constrained_columns"] == [column] and fk["referred_table"] == ref_table:
                current = ((fk.get("options") or {}).get("ondelete") or "NO ACTION").upper()
                if current == (desired or "NO ACTION").upper():
                    break  # already correct — idempotent skip
                op.drop_constraint(fk["name"], table, type_="foreignkey")
                op.create_foreign_key(
                    fk["name"], table, ref_table,
                    [column], fk["referred_columns"] or ["id"],
                    ondelete=desired,
                )
                break
        else:
            # FK constraint missing entirely (should not happen on a migrated
            # DB) — create it so the schema converges on models.py.
            op.create_foreign_key(
                f"fk_{table}_{column}", table, ref_table, [column], ["id"],
                ondelete=desired,
            )


def upgrade() -> None:
    rules = {(t, c, r): od for t, c, r, od in FK_RULES}
    _set_fk_ondelete(lambda key: rules[key])

    # Finding C — hot FK indexes (both dialects, idempotent).
    for name, table, columns in INDEXES:
        op.create_index(name, table, columns, unique=False, if_not_exists=True)


def downgrade() -> None:
    for name, table, _ in reversed(INDEXES):
        op.drop_index(name, table_name=table, if_exists=True)
    # Restore bare FKs (no ON DELETE clause) on Postgres.
    _set_fk_ondelete(lambda key: None)
